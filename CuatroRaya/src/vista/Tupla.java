package vista;

public class Tupla {
	int columna;
	boolean buena;
	
	
	
	
	public int getColumna() {
		return columna;
	}
	public void setColumna(int columna) {
		this.columna = columna;
	}
	public boolean isBuena() {
		return buena;
	}
	public void setBuena(boolean buena) {
		this.buena = buena;
	}

}
